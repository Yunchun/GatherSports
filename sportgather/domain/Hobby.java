package com.example.sportgather.domain;

public class Hobby {
    private float Year;
    private String StudentId;
    private String SportId;

    public float getYear() { return Year; }
    public void setYear(float y) { Year = y; }
    public String getStudentId() { return StudentId; }
    public void setStudentId(String s) { StudentId = s;}
    public String getSportId() { return SportId; }
    public void setSportId(String s) { SportId = s; }
}
